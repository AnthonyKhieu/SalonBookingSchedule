package model;

/**
 *
 * @author Admin
 */
public class Contact extends AbsModel{
    private int id;
    private String name;
    private String email;
    private String title;
    private String detail;

    public Contact() {
    }

    public Contact(int id, String name, String email, String title, String detail) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.title = title;
        this.detail = detail;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
    
    
}
